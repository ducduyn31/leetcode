from typing import List


def isValidSudoku(board: List[List[str]]) -> bool:
    rows = [set() for _ in range(9)]
    columns = [set() for _ in range(9)]
    boxes = [[set() for _ in range(3)] for _ in range(3)]

    for row in range(9):
        for col in range(9):
            cell = board[row][col]
            if cell.isdigit():
                cell = int(cell)

                if cell in rows[row] or cell in columns[col] or cell in boxes[row // 3][col // 3]:
                    return False

                rows[row].add(cell)
                columns[col].add(cell)
                boxes[row // 3][col // 3].add(cell)
    return True


